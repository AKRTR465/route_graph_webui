def _read_current(path: Path) -> str | None:
    try:
        return path.read_text(encoding="utf-8")
    except FileNotFoundError:
        return None


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Sync the backend OpenAPI contract for the WebUI frontend.")
    parser.add_argument("--check", action="store_true", help="Fail if the checked-in schema or DTO file is stale")
    args = parser.parse_args(argv)

    contract = _load_openapi_contract()
    rendered_schema = _format_json(contract)
    rendered_ts = _render_ts(contract)
    if args.check:
        current_schema = _read_current(SCHEMA_PATH)
        current_ts = _read_current(TS_PATH)
        if current_schema is None or current_ts is None:
            print(f"Missing API contract outputs under {SCHEMA_PATH.parent}", file=sys.stderr)
            return 1
        if current_schema != rendered_schema or current_ts != rendered_ts:
            print(
                "API schema/DTO contract is stale. Run `python tools/sync_api_schema.py`.",
                file=sys.stderr,
            )
            return 1
        return 0

    SCHEMA_PATH.parent.mkdir(parents=True, exist_ok=True)
    SCHEMA_PATH.write_text(rendered_schema, encoding="utf-8")
    TS_PATH.write_text(rendered_ts, encoding="utf-8")
    print(f"Wrote {SCHEMA_PATH.relative_to(PROJECT_ROOT).as_posix()}")
    print(f"Wrote {TS_PATH.relative_to(PROJECT_ROOT).as_posix()}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
