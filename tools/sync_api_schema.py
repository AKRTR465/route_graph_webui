TS_PATH = PROJECT_ROOT / "webui_frontend" / "src" / "types" / "api-contract.ts"
CONFIG_EXCLUDE_KEYS = {"candidate_set", "candidate_id", "candidate_ids", "output_dir"}


def _load_openapi_contract() -> dict[str, Any]:
    if str(PROJECT_ROOT) not in sys.path:
        sys.path.insert(0, str(PROJECT_ROOT))
    from webui_backend.server import app

    schema = app.openapi()
    return {
        "openapi": schema.get("openapi"),
        "info": schema.get("info"),
        "paths": schema.get("paths", {}),
        "components": {"schemas": schema.get("components", {}).get("schemas", {})},
    }


def _format_json(payload: dict[str, Any]) -> str:
    return json.dumps(payload, ensure_ascii=False, indent=2, sort_keys=True) + "\n"


def _schema_ref_name(ref: str) -> str:
    return ref.rsplit("/", 1)[-1]


def _ts_type(schema: Mapping[str, Any] | None) -> str:
    if not isinstance(schema, dict):
        return "unknown"
    if "$ref" in schema:
        return _schema_ref_name(str(schema["$ref"]))
    if "anyOf" in schema:
        parts = [_ts_type(part) for part in schema["anyOf"]]
        unique_parts: list[str] = []
        for part in parts:
            if part not in unique_parts:
                unique_parts.append(part)
        return " | ".join(unique_parts)
    if "enum" in schema and isinstance(schema["enum"], list):
        return " | ".join(json.dumps(value) for value in schema["enum"])

    schema_type = schema.get("type")
    if schema_type == "null":
        return "null"
    if schema_type == "string":
        return "string"
    if schema_type in {"number", "integer"}:
        return "number"
    if schema_type == "boolean":
        return "boolean"
    if schema_type == "array":
        item_type = _ts_type(schema.get("items"))
        if " | " in item_type:
            item_type = f"({item_type})"
        return f"{item_type}[]"
    if schema_type == "object":
        properties = schema.get("properties")
        if isinstance(properties, dict) and properties:
            required = set(schema.get("required") or [])
            parts = []
            for name, property_schema in properties.items():
                optional = "" if name in required else "?"
                parts.append(f"{json.dumps(name)}{optional}: {_ts_type(property_schema)}")
            return "{ " + "; ".join(parts) + " }"
        additional = schema.get("additionalProperties")
        if isinstance(additional, dict):
            return f"Record<string, {_ts_type(additional)}>"
        return "Record<string, unknown>"
    return "unknown"


def _render_component_types(contract: dict[str, Any]) -> list[str]:
    schemas = contract.get("components", {}).get("schemas", {})
    lines: list[str] = []
    for name in sorted(schemas):
        schema = schemas[name]
        if not isinstance(schema, dict):
            continue
        if schema.get("type") != "object":
            lines.append(f"export type {name} = {_ts_type(schema)}")
            lines.append("")
            continue

        properties = schema.get("properties") if isinstance(schema.get("properties"), dict) else {}
        required = set(schema.get("required") or [])
        lines.append(f"export interface {name} {{")
        if not properties:
            lines.append("  [key: string]: unknown")
        for property_name, property_schema in properties.items():
            optional = "" if property_name in required else "?"
            lines.append(f"  {property_name}{optional}: {_ts_type(property_schema)}")
        lines.append("}")
        lines.append("")
    return lines


def _mission_config_keys(contract: dict[str, Any]) -> tuple[str, ...]:
    preview_schema = contract.get("components", {}).get("schemas", {}).get("PreviewMissionRequest", {})
    properties = preview_schema.get("properties") if isinstance(preview_schema, dict) else {}
    if not isinstance(properties, dict):
        return ()
    return tuple(key for key in properties if key not in CONFIG_EXCLUDE_KEYS)


